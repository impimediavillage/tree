const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement | HTMLAnchorElement, // Allow anchor for Link
  React.ComponentProps<"button"> & { // Keep button props for base
    asChild?: boolean
    isActive?: boolean
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      variant = "default",
      size = "default",
      tooltip,
      className,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { isMobile, state } = useSidebar()

    const button = (
      <Comp
        ref={ref as any} // Cast to any due to complex ref type with asChild
        data-sidebar="menu-button"
        data-size={size}
        data-active={isActive}
        className={cn(sidebarMenuButtonVariants({ variant, size }), className)}
        {...props}
      />
    )

    if (!tooltip || isMobile || state === "expanded") { // Only show tooltip if collapsed and not mobile
      return button
    }
    
    const tooltipContentProps = typeof tooltip === "string" ? { children: tooltip } : tooltip;


    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          className="ml-1" // Add small margin for icon mode
          {...tooltipContentProps}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"


